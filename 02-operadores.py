### Operadores Aritméticos ###

print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 2) #operador de modulo. Te muestra el residuo en una división
print(10 // 3) #Una división que se aproxima a un número entero. Redondea. 
print(2 ** 3) #2 elevado a 3. Calcular exponentes

print("Hola " + "Python") #el simbolo mas me sirve para concatenar strings
print("Hola " * 4)

### Operadores Comparativos ###

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(4 <= 4)
print(3 == 4)
print(3 != 4)

